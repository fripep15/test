package Class_10;

public class PracticeNestedLoops {

	public static void main(String[] args) {
		for (int i = 1; i <= 3; i++) {
			System.out.println("I am outer loop");
			
			for (int j = 1; j <= 3; j++) {
				System.out.println("I am inner loop");

			}
		}
		
		System.out.println("AAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA");
		
		for (int i=1; i<=4; i++) {
			
			System.out.println("Good Morning Guys");
			
			
			for (int a=1; a<=5; a++) {
				System.out.println("Bye Bye Guys");
				
			}
		}
	}

} 
	
